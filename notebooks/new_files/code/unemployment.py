df = pd.read_csv('cleaned_unemployment_dataset.csv')

df['State'] = df['State'].map(state_code_to_name)

unemployment_columns = [
    "Unemployment_rate_2015",
    "Unemployment_rate_2016",
    "Unemployment_rate_2017",
    "Unemployment_rate_2018",
    "Unemployment_rate_2019"
]

per_educated = df.groupby('State')[unemployment_columns].mean().reset_index()

per_educated.to_csv('unemployment_by_state.csv', index=False)

